class RegisterArguments {
  final String email;
  final String phoneNo;
  final String username;

  RegisterArguments({this.email, this.phoneNo, this.username});
}

class PasswordArguments {
   final String email;
   final String phoneNo;
   final String username;
   final String password;

   PasswordArguments({this.email, this.phoneNo, this.username, this.password});
}

